def migrate():
    path = "c:/test/.claude/skills/habit-recommender/references/habit_rules.json"
    with open(path, encoding="utf-8") as f:
        data = json.load(f)

    updated = 0
    missing = []
    for goal, val in data.items():
        for h in val["habits"]:
            title = h["title"]
            pair  = EXTRA.get(title)
            if pair:
                h["effect"]    = pair[0]
                h["start_tip"] = pair[1]
                updated += 1
            else:
                missing.append(title)

    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"완료: {updated}개 업데이트")
    if missing:
        print("미등록:", missing)

if __name__ == "__main__":
    migrate()
