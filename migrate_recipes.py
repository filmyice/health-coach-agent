    updated = 0
    for goal, val in data.items():
        for food in val["foods"]:
            name = food["name"]
            existing = food.get("recipe")
            if not existing:
                continue

            # 기존 recipe를 '기본' 레시피로 변환
            base = dict(existing)
            base["label"] = "기본"

            extra = EXTRA.get((goal, name), [])
            food["recipes"] = [base] + extra
            # 하위 호환성 유지: recipe 필드 제거
